        if(!sql.equals(null)){
            try {
                //System.out.println(sql);

                //query preparing and execution
                pst = (OraclePreparedStatement) conn.prepareStatement(sql);
                rs = (OracleResultSet) pst.executeQuery(sql);

                if(rs.next()){
                    namefild.setText(rs.getString("NAME"));
                    intakefild.setText(rs.getString("INTAKE"));
                    secfild.setText(rs.getString("SEC"));
                    System.out.println("Data Matches!");
                }
                else{
                    JOptionPane.showMessageDialog(null,"No matchs!");
                    System.out.println("No Matches!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }

            try {
                //System.out.println(sql);

                //query preparing and execution
                //pst2 = (PreparedStatement) conn.prepareStatement(sql2);
                st = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_READ_ONLY);
                rs2 = (ResultSet) st.executeQuery(sql2);

                if(rs2.next()){
                    rs2.previous();
                    jTable1.setModel(JavaConnectDb.resultSetToTableModel(rs2));
                    jTable1.getColumnModel().getColumn(1).setPreferredWidth(180);
                    rs2.beforeFirst();
                    int cnt=0;
                    double grd=0,sgpa=0,tot =0, credit=0;

                    while(rs2.next()){
                        grd = rs2.getDouble("GRADE");
                        credit = rs2.getDouble("CREDIT");
                        sgpa += credit;
                        tot+= grd*credit;
                        cnt++;
                    }
                    //System.out.println(tot);
                    sgpa = tot / sgpa;
                    sgpa = round(sgpa,2);
                    sgpafild.setText(Double.toString(sgpa));
                    //System.out.println(sgpa);
                    //System.out.println(cnt);
                }
                else{
                    JOptionPane.showMessageDialog(null,"Details no matchs!");
                    System.out.println("No Matches!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,e);
            }
        }
